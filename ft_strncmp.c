#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*b;
	unsigned char	*a;

	i = 0;
	b = (unsigned char *)s2;
	a = (unsigned char *)s1;
	while ((a[i] || b[i]) && i < n)
	{
		if (a[i] != b[i])
		{
			return (a[i] - b[i]);
		}
		i++;
	}
	return (0);
}
